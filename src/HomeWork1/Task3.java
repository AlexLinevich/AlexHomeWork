package HomeWork1;

public class Task3 {

    // Изучить библиотеку (класс) Math из jdk.
    // (https://docs.oracle.com/javase/8/docs/api/java/lang/Math.html)
    public static void main(String[] args) {
        System.out.println("Абсолютное значение числа -5,2: " + Math.abs(-5.2));
        System.out.println("Максимальное из чисел 25 и 5: " + Math.max(25,5));
        System.out.println("Квадратный корень из 25: " + Math.sqrt(25));
    }
}
